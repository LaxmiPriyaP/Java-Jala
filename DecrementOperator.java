package Basics;

public class DecrementOperator {
	

	    public static void main(String[] args) {
	        int x = 5;
	        int y = --x;
	        
	        System.out.println("x = " + x);
	        System.out.println("y = " + y);
	   
	        int a = 11;
	        int b = a--;
	        
	        System.out.println("a =" + a);
	        System.out.println("b = " + b);
	    }
	}
	
